# Programa para cálculo da soma de dois números
print("Digite o primeiro operando:",end='')
a = int(input())
print("Digite o segundo operando:",end='')
b = int(input())
print(f'Resultado da soma: {a+b}')#O f é utilizado para possibilitar a utilização dos { }

